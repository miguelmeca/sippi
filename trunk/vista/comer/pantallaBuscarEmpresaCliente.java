    private void llenarTabla() {
        ArrayList<NTupla> empresas = gestor.getEmpresasCliente();
        dtm = new DefaultTableModel();
        dtm.addColumn("Nro.");
        dtm.addColumn("Razón Social");
        dtm.addColumn("CUIT");
        dtm.addColumn("E-Mail");
        dtm.addColumn("Estado");
        dtm.addColumn("Pagina Web");
        dtm.addColumn("Plantas");

        Iterator it = empresas.iterator();
        Iterator itdatos;
        while (it.hasNext()){
            NTupla tu = (NTupla)it.next();
            Object[] item = new Object[9];
            item[0] = tu.getId();
            item[1] = (String)tu.getNombre();
            ArrayList<String> datos = (ArrayList<String>)tu.getData();
            itdatos = datos.iterator();
            int i = 2;
            String dato= "";
            while(itdatos.hasNext()){
                dato = (String)itdatos.next();
                item[i] = dato;
                i++;
            }
            dtm.addRow(item);
        }
        tablaEmpresaCliente.setModel(dtm);
        elQueOrdena = new TableRowSorter<TableModel>(dtm);
        tablaEmpresaCliente.setRowSorter(elQueOrdena);
    }

    public int getIdEmpresaClienteSeleccionado(){
        int tipo = -1;
         if((tablaEmpresaCliente.getSelectedRowCount())==1)
        {
            DefaultTableModel modelo = (DefaultTableModel) tablaEmpresaCliente.getModel();
            tipo = (Integer) modelo.getValueAt(tablaEmpresaCliente.getSelectedRow(), 0);
        }
        return tipo;
    }


