    private void cargarDatosEmpresaCliente(){
        gestor.buscarEmpresaCliente();
        txtRazonSocialEC.setText(gestor.mostrarRazonSocialEC());
        txtCUITEC.setText(gestor.mostrarCUITEC());
        txtDireccionEC.setText(gestor.mostrarDireccionEC());
        txtBarrioEC.setText(gestor.mostrarBarrioEC());
        txtLocalidadEC.setText(gestor.mostrarLocalidadEC());
        txtProvinciaEC.setText(gestor.mostrarProvinciaEC());
        txtPaisEC.setText(gestor.mostrarPaisEC());
        HashSet<NTupla> tels = gestor.mostrarTelefonosEC();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Tipo");
        modelo.addColumn("Número");

        for (NTupla nTupla : tels) {
            Object[] item = new Object[2];
            item[0] = nTupla.getNombre();
            item[1] = (String)nTupla.getData();
            modelo.addRow(item);
        }
        tblTelefonosEC.setModel(modelo);
    }

    public void mostrarDatosTelefono(HashSet<NTupla> listaTelefonos) {
        DefaultTableModel tm = new DefaultTableModel();
        int i = 0;
        Object[] item = new Object[listaTelefonos.size()];
        for (NTupla tel : listaTelefonos) {
            tm.addColumn(tel.getNombre());
            item[i] = (String)tel.getData();
            i++;
        }
        tm.addRow(item);
        tblTelefonos.setModel(tm);
    }

