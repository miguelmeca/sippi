    private void tblMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMenuMouseClicked

        if(evt.getClickCount()>=2 && this.estadoCotizacion!=null && this.estadoCotizacion.equals("En Creacion"))
        {
            DefaultTableModel modelo = (DefaultTableModel)tblMenu.getModel();
            
            Tupla tp = (Tupla)modelo.getValueAt(tblMenu.getSelectedRow(), 0);
            EditarCotizacion mod = new EditarCotizacion(gestor.getGestorEditarCotizacion(tp.getId()));
            
            if(tblMenu.getSelectedRow() >=0)
                mod.setTitle("Editar Cotización: "+modelo.getValueAt(tblMenu.getSelectedRow(), 0));
            else
                mod.setTitle("Editar Cotización: Default");
            SwingPanel.getInstance().addWindow(mod);
            mod.setVisible(true);
        }
        else if(evt.getClickCount()==1 && this.estadoCotizacion!=null && this.estadoCotizacion.equals("En Creacion"))
        {
            if(tblMenu.getSelectedRowCount()!=0)
            {
                btnEditarSubObra.setEnabled(true);
                btnEliminarSubObra.setEnabled(true);
            }
            else
            {
                btnEditarSubObra.setEnabled(false);
                btnEliminarSubObra.setEnabled(false);
            }
        }
    }//GEN-LAST:event_tblMenuMouseClicked

